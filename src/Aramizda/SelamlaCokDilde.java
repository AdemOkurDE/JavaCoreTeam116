package Aramizda;

public class SelamlaCokDilde {
    public static void main(String[] args) {

        selamlaTR("Adem");
        selamlaEN("Adem");
        selamlaDE("Adem");
    }
    public static void selamlaTR(String isim){
        System.out.println("Merhaba Millet!\nBen "+isim+"!..");

    }
    public static void selamlaEN(String isim){
        System.out.println("Hello Everyone!\nI am "+isim+"!..");

    }
    public static void selamlaDE(String isim){
        System.out.println("Hallo Zusammen!\nIch bin "+isim+"!..");

    }

}
